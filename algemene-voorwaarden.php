    <h1>Algemene Voorwaarden – TL Reizen</h1>

    <h2>Artikel 1 – Toepasselijkheid</h2>
    <p>Deze algemene voorwaarden zijn van toepassing op alle vluchten en bijbehorende diensten die TL Reizen aanbiedt aan haar klanten, zowel via haar website als via andere boekingskanalen.</p>

    <h2>Artikel 2 – Boeking en Betaling</h2>
    <p>Een boeking is definitief zodra TL Reizen de betaling heeft ontvangen en de klant een bevestiging heeft ontvangen.<br>
        De klant is verantwoordelijk voor het verstrekken van correcte gegevens bij het boeken.<br>
        Betalingen dienen vooraf te worden voldaan via de aangeboden betaalmethoden.</p>

    <h2>Artikel 3 – Wijziging en Annulering</h2>
    <p>Wijzigingen in boekingen zijn mogelijk conform de geldende tariefvoorwaarden.<br>
        Annulering is alleen mogelijk onder de voorwaarden zoals vermeld tijdens het boeken. Restitutie is afhankelijk van het gekozen tarief en kan administratiekosten met zich meebrengen.</p>

    <h2>Artikel 4 – Reisdocumenten en Inchecken</h2>
    <p>Passagiers zijn zelf verantwoordelijk voor geldige reisdocumenten (zoals paspoort, visa, vaccinatiebewijzen).<br>
        Inchecktijden worden gecommuniceerd via de website of e-mail. Te laat arriveren kan leiden tot weigering tot instappen zonder recht op restitutie.</p>

    <h2>Artikel 5 – Bagage</h2>
    <p>Hand- en ruimbagage zijn onderhevig aan beperkingen zoals vermeld op onze website.<br>
        Overbagage en bijzondere bagage (zoals sportuitrusting) kunnen extra kosten met zich meebrengen.</p>

    <h2>Artikel 6 – Aansprakelijkheid</h2>
    <p>TL Reizen is niet aansprakelijk voor schade of verlies veroorzaakt door vertragingen, annuleringen of overmachtssituaties.<br>
        Eventuele schadeclaims worden behandeld volgens het Verdrag van Montreal en de Europese luchtvaartregelgeving (EU 261/2004).</p>

    <h2>Artikel 7 – Gedrag aan boord</h2>
    <p>Passagiers zijn verplicht zich te houden aan instructies van het cabinepersoneel.<br>
        Wangedrag kan leiden tot weigering van verdere dienstverlening en/of aangifte bij autoriteiten.</p>

    <h2>Artikel 8 – Klachten</h2>
    <p>Klachten kunnen binnen 14 dagen na de betreffende vlucht worden ingediend via onze klantenservice.</p>

    <h2>Artikel 9 – Toepasselijk recht</h2>
    <p>Op deze voorwaarden is Nederlands recht van toepassing. Geschillen worden voorgelegd aan de bevoegde rechter in Nederland.</p>
